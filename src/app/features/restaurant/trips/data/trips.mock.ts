function trip(partial: DeliveryTrip): DeliveryTrip {
  const boxCount = partial.stops.reduce((sum, s) => sum + s.boxCount, 0);
  return {
    ...partial,
    boxCount,
    customerCount: partial.stops.length,
  };
}

