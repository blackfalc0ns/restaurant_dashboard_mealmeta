const CATEGORY_LABELS: Record<
  IngredientCategory,
  { ar: string; en: string }
> = {
  poultry: { ar: 'دواجن / لحوم', en: 'Poultry / meat' },
  seafood: { ar: 'مأكولات بحرية', en: 'Seafood' },
  dairy: { ar: 'ألبان وبيض', en: 'Dairy & eggs' },
  produce: { ar: 'خضار وفواكه', en: 'Produce' },
  grains: { ar: 'حبوب', en: 'Grains' },
  other: { ar: 'أخرى', en: 'Other' },
};

const ALLERGEN_LABELS: Record<string, { ar: string; en: string }> = {
  dairy: { ar: 'حليب', en: 'Dairy' },
  eggs: { ar: 'بيض', en: 'Eggs' },
  nuts: { ar: 'مكسرات', en: 'Nuts' },
  fish: { ar: 'سمك', en: 'Fish' },
  gluten: { ar: 'غلوتين', en: 'Gluten' },
  sesame: { ar: 'سمسم', en: 'Sesame' },
};

function item(
  partial: Omit<IngredientItem, 'categoryLabel' | 'allergenLabels'> & {
    allergenKeys: string[];
  },
): IngredientItem {
  return {
    ...partial,
    categoryLabel: CATEGORY_LABELS[partial.category],
    allergenLabels: partial.allergenKeys.map(
      (key) => ALLERGEN_LABELS[key] ?? { ar: key, en: key },
    ),
  };
}

